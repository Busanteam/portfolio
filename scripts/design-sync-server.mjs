#!/usr/bin/env node
/**
 * Portfolio local dev server with Design Sync
 * - Serves page/ on http://localhost:3847
 * - Saves DevTools inline style edits to portfolio.overrides.css
 * - Live reload when source files change
 */
import http from 'http';
import https from 'https';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const PAGE_ROOT = path.join(ROOT, 'page');
const OVERRIDES_PATH = path.join(PAGE_ROOT, 'assets/css/portfolio.overrides.css');
const PORT = Number(process.env.PORT || 3847);
const HOST = '127.0.0.1';

const MIME = {
    '.html': 'text/html; charset=utf-8',
    '.css': 'text/css; charset=utf-8',
    '.js': 'application/javascript; charset=utf-8',
    '.json': 'application/json; charset=utf-8',
    '.svg': 'image/svg+xml',
    '.png': 'image/png',
    '.jpg': 'image/jpeg',
    '.jpeg': 'image/jpeg',
    '.webp': 'image/webp',
    '.ico': 'image/x-icon',
    '.woff2': 'font/woff2',
    '.woff': 'font/woff',
    '.ttf': 'font/ttf'
};

const sseClients = new Set();
// portfolio.overrides.css is excluded — Design Sync writes it; watching would reload on every pick/save.
const WATCH_PATHS = [
    'index.html',
    'assets/css/portfolio.css',
    'assets/js/portfolio.js',
    'assets/js/design-sync.js'
];

function sendJson(res, status, data) {
    res.writeHead(status, {
        'Content-Type': 'application/json; charset=utf-8',
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
        'Access-Control-Allow-Headers': 'Content-Type'
    });
    res.end(JSON.stringify(data));
}

function readBody(req) {
    return new Promise((resolve, reject) => {
        const chunks = [];
        req.on('data', c => chunks.push(c));
        req.on('end', () => resolve(Buffer.concat(chunks).toString('utf8')));
        req.on('error', reject);
    });
}

function parseRulesFromCss(css) {
    const map = new Map();
    const cleaned = css.replace(/\/\*[\s\S]*?\*\//g, '');
    const blocks = cleaned.match(/([^{}]+)\{([^}]*)\}/g) || [];
    blocks.forEach(block => {
        const m = block.match(/([^{}]+)\{([^}]*)\}/);
        if (!m) return;
        const selector = m[1].trim();
        if (!selector || selector.startsWith('@')) return;
        m[2].split(';').forEach(decl => {
            const parts = decl.split(':');
            if (parts.length < 2) return;
            const prop = parts[0].trim();
            const value = parts.slice(1).join(':').trim();
            if (prop && value) map.set(`${selector}|||${prop}`, { selector, prop, value });
        });
    });
    return map;
}

function serializeRules(map) {
    const bySelector = new Map();
    map.forEach(({ selector, prop, value }) => {
        if (!bySelector.has(selector)) bySelector.set(selector, []);
        bySelector.get(selector).push({ prop, value });
    });

    const header = `/**\n * Design Sync overrides\n * Auto-generated by npm run dev — ${new Date().toISOString()}\n */\n\n`;
    if (bySelector.size === 0) return header;

    const body = [...bySelector.entries()].map(([selector, decls]) => {
        const lines = decls.map(d => `    ${d.prop}: ${d.value};`).join('\n');
        return `${selector} {\n${lines}\n}`;
    }).join('\n\n');

    return header + body + '\n';
}

async function saveOverrides(updates) {
    let existing = '';
    try {
        existing = await fs.promises.readFile(OVERRIDES_PATH, 'utf8');
    } catch {
        existing = '';
    }

    const map = parseRulesFromCss(existing);
    updates.forEach(({ selector, property, value, remove }) => {
        if (!selector || !property) return;
        const key = `${selector}|||${property}`;
        if (remove || value === '' || value === null || value === undefined) {
            map.delete(key);
            return;
        }
        map.set(key, { selector, prop: property, value: String(value).trim() });
    });

    const output = serializeRules(map);
    await fs.promises.mkdir(path.dirname(OVERRIDES_PATH), { recursive: true });
    await fs.promises.writeFile(OVERRIDES_PATH, output, 'utf8');
    return { saved: updates.length, path: OVERRIDES_PATH };
}

async function postJson(url, headers, payload) {
    const body = JSON.stringify(payload);
    const parsed = new URL(url);

    if (globalThis.fetch) {
        const res = await fetch(url, {
            method: 'POST',
            headers: { ...headers, 'Content-Type': 'application/json' },
            body
        });
        return { status: res.status, body: await res.text() };
    }

    const transport = parsed.protocol === 'https:' ? https : http;
    return new Promise((resolve, reject) => {
        const req = transport.request({
            hostname: parsed.hostname,
            port: parsed.port || (parsed.protocol === 'https:' ? 443 : 80),
            path: `${parsed.pathname}${parsed.search}`,
            method: 'POST',
            headers: {
                ...headers,
                'Content-Type': 'application/json',
                'Content-Length': Buffer.byteLength(body)
            }
        }, res => {
            const chunks = [];
            res.on('data', chunk => chunks.push(chunk));
            res.on('end', () => resolve({ status: res.statusCode || 0, body: Buffer.concat(chunks).toString('utf8') }));
        });
        req.on('error', reject);
        req.write(body);
        req.end();
    });
}

const CSS_AI_SYSTEM = `You are a CSS assistant for a local portfolio Design Sync dev tool.
The user selected an HTML element and describes visual changes in natural language (Korean or English).

Return ONLY valid JSON (no markdown fences):
{
  "commands": [
    { "property": "kebab-case-css-property", "value": "css value" },
    { "property": "property-name", "remove": true }
  ],
  "explanation": "Brief Korean summary of what was applied"
}

Rules:
- Prefer CSS commands the browser can apply via element.style
- Use remove:true to unset properties
- If HTML structure must change (not just CSS), return empty commands and put instructions in htmlHint
- Do not invent selectors; commands apply to the already-selected element only`;

function buildUserPrompt({ prompt, selector, context }) {
    return `Selector: ${selector}
Element: <${context.tagName}> class="${context.className}" id="${context.id || ''}"
Current inline style: ${context.inlineStyle || '(none)'}
Text preview: ${context.textPreview || ''}

User request:
${prompt}`;
}

function parseAiJson(content) {
    const parsed = JSON.parse(content);
    if (!Array.isArray(parsed.commands)) parsed.commands = [];
    return parsed;
}

async function callOpenAiForCss({ prompt, selector, context, apiKey }) {
    const key = apiKey.trim();
    const model = process.env.OPENAI_MODEL || 'gpt-4o-mini';
    const user = buildUserPrompt({ prompt, selector, context });

    const { status, body: responseText } = await postJson(
        'https://api.openai.com/v1/chat/completions',
        { Authorization: `Bearer ${key}` },
        {
            model,
            temperature: 0.2,
            response_format: { type: 'json_object' },
            messages: [
                { role: 'system', content: CSS_AI_SYSTEM },
                { role: 'user', content: user }
            ]
        }
    );

    if (status < 200 || status >= 300) {
        throw new Error(`OpenAI API error (${status}): ${responseText.slice(0, 300)}`);
    }

    const data = JSON.parse(responseText);
    const content = data.choices?.[0]?.message?.content;
    if (!content) throw new Error('AI 응답이 비어 있습니다.');
    return parseAiJson(content);
}

async function callGeminiForCss({ prompt, selector, context, apiKey }) {
    const key = apiKey.trim();
    const model = process.env.GEMINI_MODEL || 'gemini-2.0-flash';
    const user = buildUserPrompt({ prompt, selector, context });
    const url = `https://generativelanguage.googleapis.com/v1beta/models/${model}:generateContent?key=${encodeURIComponent(key)}`;

    const { status, body: responseText } = await postJson(url, {}, {
        systemInstruction: { parts: [{ text: CSS_AI_SYSTEM }] },
        contents: [{ role: 'user', parts: [{ text: user }] }],
        generationConfig: {
            temperature: 0.2,
            responseMimeType: 'application/json'
        }
    });

    if (status < 200 || status >= 300) {
        throw new Error(`Gemini API error (${status}): ${responseText.slice(0, 300)}`);
    }

    const data = JSON.parse(responseText);
    const content = data.candidates?.[0]?.content?.parts?.[0]?.text;
    if (!content) {
        const block = data.candidates?.[0]?.finishReason || data.promptFeedback?.blockReason;
        throw new Error(`Gemini 응답이 비어 있습니다.${block ? ` (${block})` : ''}`);
    }
    return parseAiJson(content);
}

function resolveApiKey(panelKey) {
    return (panelKey || process.env.GEMINI_API_KEY || process.env.OPENAI_API_KEY || '').trim();
}

function detectProvider(key) {
    if (key.startsWith('sk-')) return 'openai';
    return 'gemini';
}

async function callAiForCss({ prompt, selector, context, apiKey }) {
    const key = resolveApiKey(apiKey);
    if (!key) {
        throw new Error('API Key가 필요합니다. GEMINI_API_KEY 환경변수 또는 패널에 Gemini/OpenAI 키를 입력하세요.');
    }

    if (detectProvider(key) === 'openai') {
        if (!/^sk-[A-Za-z0-9_-]{10,}$/.test(key)) {
            throw new Error('OpenAI API 키 형식이 아닙니다. sk- 로 시작해야 합니다.');
        }
        return callOpenAiForCss({ prompt, selector, context, apiKey: key });
    }

    return callGeminiForCss({ prompt, selector, context, apiKey: key });
}

function broadcastReload() {
    const payload = `data: ${JSON.stringify({ type: 'reload', at: Date.now() })}\n\n`;
    sseClients.forEach(res => {
        try { res.write(payload); } catch { sseClients.delete(res); }
    });
}

function safePath(urlPath) {
    const decoded = decodeURIComponent(urlPath.split('?')[0]);
    const rel = decoded.replace(/^\/+/, '') || 'index.html';
    const abs = path.resolve(PAGE_ROOT, rel);
    if (!abs.startsWith(PAGE_ROOT)) return null;
    return abs;
}

function serveStatic(req, res, filePath) {
    fs.readFile(filePath, (err, data) => {
        if (err) {
            res.writeHead(404, { 'Content-Type': 'text/plain; charset=utf-8' });
            res.end('Not Found');
            return;
        }
        const ext = path.extname(filePath).toLowerCase();
        res.writeHead(200, { 'Content-Type': MIME[ext] || 'application/octet-stream' });
        res.end(data);
    });
}

const server = http.createServer(async (req, res) => {
    const url = new URL(req.url, `http://${req.headers.host}`);

    if (req.method === 'OPTIONS') {
        res.writeHead(204, {
            'Access-Control-Allow-Origin': '*',
            'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
            'Access-Control-Allow-Headers': 'Content-Type'
        });
        res.end();
        return;
    }

    if (url.pathname === '/__design-sync/health') {
        sendJson(res, 200, {
            ok: true,
            port: PORT,
            overrides: OVERRIDES_PATH,
            aiConfigured: !!(process.env.GEMINI_API_KEY || process.env.OPENAI_API_KEY),
            aiProviders: ['gemini', 'openai']
        });
        return;
    }

    if (url.pathname === '/__design-sync/events' && req.method === 'GET') {
        res.writeHead(200, {
            'Content-Type': 'text/event-stream',
            'Cache-Control': 'no-cache',
            Connection: 'keep-alive',
            'Access-Control-Allow-Origin': '*'
        });
        res.write(`data: ${JSON.stringify({ type: 'connected' })}\n\n`);
        sseClients.add(res);
        req.on('close', () => sseClients.delete(res));
        return;
    }

    if (url.pathname === '/__design-sync/ai-command' && req.method === 'POST') {
        try {
            const raw = await readBody(req);
            const body = JSON.parse(raw || '{}');
            const result = await callAiForCss({
                prompt: body.prompt || '',
                selector: body.selector || '',
                context: body.context || {},
                apiKey: body.apiKey || ''
            });
            sendJson(res, 200, { ok: true, ...result });
        } catch (e) {
            sendJson(res, 500, { ok: false, error: e.message });
        }
        return;
    }

    if (url.pathname === '/__design-sync/save-css' && req.method === 'POST') {
        try {
            const raw = await readBody(req);
            const body = JSON.parse(raw || '{}');
            const updates = Array.isArray(body.updates) ? body.updates : [];
            const result = await saveOverrides(updates);
            sendJson(res, 200, { ok: true, ...result });
        } catch (e) {
            sendJson(res, 500, { ok: false, error: e.message });
        }
        return;
    }

    const filePath = safePath(url.pathname);
    if (!filePath) {
        res.writeHead(403);
        res.end('Forbidden');
        return;
    }

    fs.stat(filePath, (err, stat) => {
        if (!err && stat.isDirectory()) {
            serveStatic(req, res, path.join(filePath, 'index.html'));
            return;
        }
        if (err) {
            if (url.pathname === '/' || url.pathname.endsWith('/')) {
                serveStatic(req, res, path.join(PAGE_ROOT, 'index.html'));
                return;
            }
            res.writeHead(404);
            res.end('Not Found');
            return;
        }
        serveStatic(req, res, filePath);
    });
});

server.listen(PORT, HOST, () => {
    console.log('');
    console.log('  Portfolio Design Sync Dev Server');
    console.log('  --------------------------------');
    console.log(`  Local:   http://${HOST}:${PORT}/`);
    console.log(`  Saves:   page/assets/css/portfolio.overrides.css`);
    console.log('');
    console.log('  개발자도구에서 style 수정 → portfolio.overrides.css 자동 저장 (저장 시 새로고침 없음)');
    console.log('  CSS/JS/HTML 소스 파일 저장 시에만 페이지 자동 새로고침');
    console.log('  AI:      Gemini (AQ./AIza...) 또는 OpenAI (sk-...)');
    const geminiEnv = process.env.GEMINI_API_KEY ? 'GEMINI ✓' : '';
    const openaiEnv = process.env.OPENAI_API_KEY ? 'OPENAI ✓' : '';
    console.log(`  AI Key:  ${[geminiEnv, openaiEnv].filter(Boolean).join(' ') || '패널 입력 또는 GEMINI_API_KEY 환경변수'}`);
    console.log('');
});

WATCH_PATHS.forEach(rel => {
    const abs = path.join(PAGE_ROOT, rel);
    fs.watch(abs, { persistent: true }, (eventType) => {
        if (eventType === 'change') broadcastReload();
    });
});
